#operadores de pertenencia (in, not in)

frutas = ["manzana","pera","naranja"]
frutas2 = "pera"

x=(frutas2 in frutas)

print(x)

print(frutas2 in frutas)#pera si esta en el array

print("----------")
#not in

x=(frutas2 not in frutas)

print(x)

print(frutas2 not in frutas) 

frutas3="melocoton"

print(frutas3 not in frutas) #melocoton no esta en el array



